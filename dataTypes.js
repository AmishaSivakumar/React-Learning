// 8 data Types in js

// String
let username = "Amisha"
console.log(username)

// Number
let number = 5554112
console.log(number)

// Array >> Group of data with different data types
let array = [1, 8, 1, "sbshfb", {}]
console.log(array);
console.log(array[3])

//  Boolean >> True or false
let condition = true || false
console.log(condition);

// Object 
let details = {
    name: "amisha",
    number: 8521457,
    email: "amishasivakumar@gmail.com",
    password: "jshwiyr67457"
}
console.log(details);
console.log(details.number); // "." object notation

// Null
let value = null
console.log(null)

// Undefined
let data = undefined
console.log(data);

